    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtCodigo.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "debes ingresar un codigo, es obligatorio", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocus();
            return;
        }

        if (txtNombre.getText().length() == 0 || txtNombre.getText().length() > 40) {
            JOptionPane.showMessageDialog(this, "debes ingresar un nombre, es obligatorio y tiene que tener menos de 20 caracteres", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocus();
            return;
        }
        if (txtCantidad.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "debes de igresar una cantidad entre 0 y 150000", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocus();
            return;
        }

        String resp;//Variable de respuesta
        if (this.accion.equals("editar")) {

            Referencia seleccionado = (Referencia) cboReferencia.getSelectedItem();

            resp = this.CONTROL.actualizar(Integer.parseInt(txtId.getText()), seleccionado.getId(), txtCodigo.getText(), txtNombre.getText(), this.nombreAnt, Integer.parseInt(txtCantidad.getText()));
            if (resp.equals("OK")) {

                this.mensajeOk("Actualizado Correctamnte");
                this.limpiar();
                this.Listar("", false);
                tabGeneral.setSelectedIndex(0);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setEnabledAt(0, true);

            } else {
                this.mensajeError(resp);
            }
            // editar
        } else {
            //guardar
            Referencia seleccionado = (Referencia) cboReferencia.getSelectedItem();

            resp = this.CONTROL.insertar(seleccionado.getId(), txtCodigo.getText(), txtNombre.getText(), Integer.parseInt(txtCantidad.getText()));
            if (resp.equals("OK")) {

                this.mensajeOk("Registrado Correctamnte");
                this.limpiar();
                this.Listar("", false);
                tabGeneral.setSelectedIndex(0);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setEnabledAt(0, true);

            } else {
                this.mensajeError(resp);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

