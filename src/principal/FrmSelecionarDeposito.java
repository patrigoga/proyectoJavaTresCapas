    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tablaListado.getSelectedColumnCount() == 1) {

            String id = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 0));

            int referenciaId = Integer.parseInt(String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 1)));

            String referenciaNombre = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 2));

            String codigo = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 3));

            String nombre = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 4));

            this.nombreAnt = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 4));

            String stock = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 5));

            txtId.setText(id);
            Referencia seleccionado = new Referencia(referenciaId, referenciaNombre);
            cboReferencia.setSelectedItem(seleccionado);
            txtCodigo.setText(codigo);
            txtNombre.setText(nombre);
            txtCantidad.setText(stock);

            tabGeneral.setEnabledAt(0, false);
            tabGeneral.setEnabledAt(1, true);
            tabGeneral.setSelectedIndex(1);
            this.accion = "editar";
            btnGuardar.setText("Editar");

        } else {
            this.mensajeError("Seleccione 1 registro a editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

